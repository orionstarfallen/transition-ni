  function toggleDarkButton(){
    var donate_button = document.getElementById("donate-button");

    donate_button.style.background = "whitesmoke";
    donate_button.style.color = "black";
  }

  function toggleLightButton(){
    var donate_button = document.getElementById("donate-button");

    donate_button.style.background = "black";
    donate_button.style.color = "white";
  }